# Comparison operator
a = 10
b = 3
print(a > b)
print(a < b)
print(a >= b)
print(a <= b)
print(a == b)
print(a != b)

# Logical operator
a = True
b = False
print(a and b)
print(a or b)
print(not a)

# Bitwise operator
a = 10
b = 3
print(a & b)
print(a | b)
print(a ^ b)
print(~a)
print(a << 2)
print(a >> 2)

# Membership operator
a = 10
b = 3
print(a in [1, 2, 3])
print(a not in [1, 2, 3])

# Identity operator
a = 10
b = 10
print(a is b)
print(a is not b)

# Ternary operator
a = 10
b = 3
print(a if a > b else b)

